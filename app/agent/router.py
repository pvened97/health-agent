def classify_intent(message: str, has_image: bool = False) -> str:
    """Определяет тип интента для выбора промпта и набора tools.

    Если сообщение содержит паттерны из нескольких категорий (еда + тренировка,
    сон + еда и т.д.) — возвращает GENERAL, у которого есть все tools.
    """
    if has_image:
        return INTENT_FOOD_PHOTO

    lower = message.lower().strip()

    # Собираем все сработавшие категории (без исключений)
    matched_intents: list[str] = []

    is_food_excluded = any(p in lower for p in _FOOD_EXCLUDE_PATTERNS)
    if not is_food_excluded:
        if any(p in lower for p in _FOOD_LOGGING_PATTERNS):
            matched_intents.append(INTENT_FOOD_TEXT)

    is_workout_excluded = any(p in lower for p in _WORKOUT_EXCLUDE_PATTERNS)
    if not is_workout_excluded:
        if any(p in lower for p in _WORKOUT_LOGGING_PATTERNS):
            matched_intents.append(INTENT_WORKOUT)

    is_body_excluded = any(p in lower for p in _BODY_STATE_EXCLUDE_PATTERNS)
    if not is_body_excluded:
        if any(p in lower for p in _BODY_STATE_LOGGING_PATTERNS):
            matched_intents.append(INTENT_BODY_STATE)

    # Если сработало больше одной категории записи — general (все tools)
    if len(matched_intents) > 1:
        logger.info("Intent → general (mixed: %s)", matched_intents)
        return INTENT_GENERAL

    if len(matched_intents) == 1:
        intent = matched_intents[0]
        logger.info("Intent → %s", intent)
        return intent

    # Рекомендации, аналитика, итоги
    for pattern in _ADVICE_PATTERNS:
        if pattern in lower:
            logger.info("Intent → advice (pattern: %r)", pattern)
            return INTENT_ADVICE

    return INTENT_GENERAL


